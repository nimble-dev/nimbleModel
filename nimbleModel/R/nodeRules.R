## The nodeRuleClass is the base class for declRuleClass, calcRuleClass, and rhsRuleClass.
## These classes represent the indexing of one or more nodes in a model,
## distinguishing indexing across nodes (external indexing) from indexing
## within a node (internal indexing).

## Unlike a graphRule, a nodeRule does not translate indexing between variables;
## rather it simply represents the valid index values for a variable.

## There is one declRule for each declaration in a model, representing the indexing for the LHS variable.
## A declRule contains the `calculate` function (i.e., the nodeFunction) that operates on a
## single set of index values.

## calcRules are generated by starting with declRules and then fracturing (with `fracture`)
## based on top-down processing. This produces a calcRule for each set of nodes
## from a declaration that can be calculated together (same sortID)
## (as well as the special case of state-space model type formulations).

## TODO: work on `simulate`, `getParam` and other related model methods; will these be part or
## `calcRules`?

## nodeRanges are similar to varRanges but account for sets of elements tied together by a declaration
## via having both external and internal indexing. E.g., `x[i, 1:3]` has external indexing for the
## first index slot and internal indexing for the `1:3` block.

## calcRanges manage the calculation for one or more nodes, handling the indexing, and
## calling out to the declRule `calculate` function.

## Base class for all node-related classes
nodeRuleClass <- R6Class(
    classname = "nodeRuleClass",
    portable = FALSE,
    public = list(
        context = NULL,
        expr = NULL,  
        constants = NULL,
        ID = character(),  # need to look up calcRules by name not position
        varName = character(),
        fullRule = NULL,
        ## `externalRule` and `internalRule` are subsets of the fullRule,
        ## so sets of indexRules and associated information.
        externalRule = list(), # indexing for the nodes
        internalRule = list(), # indexing for components of multivariate nodes
        numExternalIndexRules = 0,
        numInternalIndexRules = 0,
        indexSlotToSet = NULL,

        initialize = function(expr, ID, context = modelContextClass$new(), constants = list()) {
            varName <<- getVarName(expr)
            ID <<- as.character(ID)
            context <<- context
            expr <<- expr
            constants <<- constants

            ## Note: this is awkward to go into the data structures and modify them

            ## We'll use graphRules, though only have a single side of declaration.
            fullRule <<- graphRuleClass$new(expr, expr, context, constants)
            indexSlotToSet <<- fullRule$indexSets$toIndexSlotToSet

            if(length(fullRule$indexRules)) {  # if any indexing
                isConstant <- sapply(fullRule$indexRules, function(x) is(x, "indexRuleConstantClass"))

                ## Treat constants as internal rules that don't relate to indexing over nodes.
                ## Need to relate constant rule types to indexing; constant rules are in order of constant indices.
                constantIndices <- fullRule$indexSets$toIndexSlotToSet == 0

                externalRule <<- fullRule$clone()
                externalRule$indexRules[isConstant] <<- NULL
                externalRule$indexSets$toIndexSlotToSet <<-
                    externalRule$indexSets$toIndexSlotToSet[!constantIndices]
                
                internalRule <<- fullRule$clone()
                internalRule$indexRules[!isConstant] <<- NULL

                ## Transform indexSets as if LHS is just from the constant rules and RHS is
                ## all constants. E.g., `y[1] <- y[1,1]` if one of two slots is constant.
                toExpr <- parse(text = paste0("y[", paste(rep(1, sum(constantIndices)), collapse = ',')  , "]"))[[1]]
                fromExpr <- parse(text = paste0("y[", paste(rep(1, length(constantIndices)), collapse = ',')  , "]"))[[1]]
                internalRule$indexSets <- makeSeparableIndexSets(toExpr, fromExpr, modelContextClass$new())

                numExternalIndexRules <<- length(externalRule$indexRules)
                numInternalIndexRules <<- length(internalRule$indexRules)
            }
        },
        
        ## Generate nodeRange from a varRange, nodeRange, or char representation of a varRange (e.g., 'y[1:3]'.
        apply = function(varRange = NULL) {
            if(is.null(varRange)) 
                varRange <- varName                
            name <- getVarName(varRange)
            if(name != varName)  # variable names don't match
                return(NULL)  
            if(is.character(varRange)) 
                if(name == varRange) {
                    varRange <- fullRule$getFromRange()  # e.g., 'y'
                } else varRange <- varRangeClass$new(varRange)  # e.g., 'y[1:3]'

            ## Apply both the external and internal indexRules.
            if(numExternalIndexRules) {
                externalRange <- externalRule$apply(varRange)
                if(is.null(externalRange)) return(NULL)
            } else externalRange <- varRangeClass$new(list())
            if(numInternalIndexRules) {
                internalRange <- internalRule$apply(varRange)
                if(is.null(internalRange)) return(NULL)
            } else internalRange <- varRangeClass$new(list())

            return(
                nodeRangeClass$new(varName, externalRange, internalRange,
                                   indexSlotToSet, self)
            )
        },

        getFullRange = function() {
            return(fullRule$apply(varName))
        }
    )
)

